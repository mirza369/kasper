                small pieces of data, stored in text files, that are stored on your computer or
                other device when websites are loaded in a browser. They are widely used to
                “remember” you and your preferences, either for a single visit (through a
                “session cookie”) or for multiple repeat visits (using a “persistent cookie”).
                They ensure a consistent and efficient experience for visitors, and perform
                essential functions such as allowing users to register and remain logged in.
                Cookies may be set by the site that you are visiting (known as “first party
                cookies”), or by third parties, such as those who serve content or provide
                advertising or analytics services on the website (“third party cookies”).</span></p>
